package week2homework;
//Write a Java program to convert a given string into lowercase. Sample
//Input: THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG.
public class UppercaseToLowerCase {
    public static void main(String[] args) {  //main method

        String name = "THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG";  //local variable
        System.out.println("Lower case   "+name.toLowerCase());  //changed local uppercase to lower case

    }
}
