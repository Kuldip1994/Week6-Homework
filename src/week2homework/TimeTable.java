package week2homework;
//Write a Java program that takes a number as input and prints its
//multiplication table up to 10.
public class TimeTable {

    public static void main(String[] args) {

        int n=8;
        for (int i = 1; i <= 10; i++) {

            System.out.println(n + " *" + i +" = "+n *i);
        }

    }
}
